//program for right shift , left shift and unsigned right operator
//syntax is.............  number shift_operator number_of_places_to_shift

public class JavaOperators_BitShift
{
	public static void main(String args[])
	{
		int a=58;
		System.out.println(a<<2);   //left shift
		System.out.println(a>>2);   //right shift
		System.out.println(a>>>2);  //unsigned right shift 
		
	}
} 